int	ft_atoi(const char *nptr)
{
	int	ret;
	int	sg;
	int	i;

	i = 0;
	ret = 0;
	sg = 1;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || (nptr[i] == 32))
		i++;
	if (nptr[i] == '+')
		i++;
	else if (nptr[i] == '-')
	{
		sg *= -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		ret *= 10;
		ret += nptr[i] - 48;
		i++;
	}
	return (ret * sg);
}
